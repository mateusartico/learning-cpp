#include <iostream>
#include <cmath> // Include the cmath library to use common math functions

using namespace std;

int main() {
    cout << max(5, 10) << endl;   // Returns the larger of 5 and 10
    cout << min(5, 10) << endl;   // Returns the smaller of 5 and 10
    cout << sqrt(64) << endl;     // Returns the square root of 64 (result: 8)
    cout << round(2.6) << endl;   // Rounds 2.6 to the nearest whole number (result: 3)
    cout << log(2) << endl;       // Returns the natural logarithm (base e) of 2

    return 0;
}
